package br.com.brincando;

public class Declarando {
	public static void main(String[] args) {
		int numero1 = 10;
		Integer numero03 = 50;
		
		double numero02 = 10.50;
		Double numero04 = 50.00;
		
		String texto = "Olá, Itamar aqui!";
		
		Boolean verdade = false;
		
		System.out.println("Estou declarando variaveis, segue abaixo:");
		
		System.out.println("Número 01: " + numero1);
		System.out.println("Número 03: " + numero03);
		System.out.println("Número 02: " + numero02);
		System.out.println("Número 04: " + numero04);
		
		System.out.println(texto);
		
		System.out.println("Verdade: " + verdade);
	}
}
